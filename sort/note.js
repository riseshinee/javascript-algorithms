/**
 * 거품정렬: 가장 간단한 정렬 알고리즘, 전체 배열을 순회하면서 항목이 다른 항목보다 큰  경두 항목을 교환
 * 시간복잡도: O(n^2)
 */

/**
 * 선택정렬: 가장 작은 항목을 찾아서 해당 항목을 배열의 현 위치에 삽입
 * 시간복잡도: O(n^2)
 */

/**
 * 정렬 요약
 * 자바스크립트 엔진 정렬 알고리즘은 병합정렬 또는 빠른정렬을 사용함, 시간복잡도 O(nlog2(n))
 * 빠른정렬: O(nlog2(n))
 * 병합정렬: O(nlog2(n))
 * 거품정렬: O(n^2)
 * 삽입정렬: O(n^2)
 * 선택정렬: O(n^2)
 * 계수정렬: O(k+n)
 */